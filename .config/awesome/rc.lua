-- {{{ Autostart windowless processes

-- This function will run once every time Awesome is started
local function run_once(cmd_arr)
    for _, cmd in ipairs(cmd_arr) do
        awful.spawn.with_shell(string.format("pgrep -u $USER -fx '%s' > /dev/null || (%s)", cmd, cmd))
    end
end

local swedish_keyboard_layout = true
local function toggle_keyboard_layout()
    swedish_keyboard_layout = not swedish_keyboard_layout
    if swedish_keyboard_layout then
        awful.spawn.with_shell("setxkbmap se")
    else
        awful.spawn.with_shell("setxkbmap us")
    end
end

